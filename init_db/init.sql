CREATE TABLE Student(
   cip VARCHAR(8),
   lastname VARCHAR(50),
   firstname VARCHAR(50),
   email VARCHAR(50),
   ssh VARCHAR(250),
   PRIMARY KEY(cip)
);

CREATE TABLE Project(
   id_project SERIAL,
   PRIMARY KEY(id_project)
);

CREATE TABLE Event(
   id_event VARCHAR(50),
   cip VARCHAR(50),
   date_event timestamp,
   id_project INTEGER,
   PRIMARY KEY(id_event),
   FOREIGN KEY(id_project) REFERENCES Project(id_project)
);

CREATE TABLE File(
   id_file VARCHAR(50),
   name VARCHAR(50),
   size BIGINT,
   last_change timestamp,
   id_project INTEGER NOT NULL,
   PRIMARY KEY(id_file),
   FOREIGN KEY(id_project) REFERENCES Project(id_project)
);

CREATE TABLE Tutor(
   cip VARCHAR(8),
   firstname VARCHAR(50),
   lastname VARCHAR(50),
   email VARCHAR(50),
   ssh VARCHAR(250),
   PRIMARY KEY(cip)
);

CREATE TABLE Course(
   sigle VARCHAR(50),
   name VARCHAR(100),
   team_size INTEGER,
   remise VARCHAR(50),
   PRIMARY KEY(sigle)
);

CREATE TABLE Student_Project(
   cip VARCHAR(8),
   id_project INTEGER,
   PRIMARY KEY(cip, id_project),
   FOREIGN KEY(cip) REFERENCES Student(cip),
   FOREIGN KEY(id_project) REFERENCES Project(id_project)
);

CREATE TABLE Student_Course(
   cip VARCHAR(8),
   sigle VARCHAR(50),
   PRIMARY KEY(cip, sigle),
   FOREIGN KEY(cip) REFERENCES Student(cip),
   FOREIGN KEY(sigle) REFERENCES Course(sigle) 
);

CREATE TABLE Tutor_Course(
   cip VARCHAR(8),
   sigle VARCHAR(50),
   PRIMARY KEY(cip, sigle),
   FOREIGN KEY(cip) REFERENCES Tutor(cip),
   FOREIGN KEY(sigle) REFERENCES Course(sigle)
);

CREATE TABLE Course_Project(
   sigle VARCHAR(50),
   id_project INTEGER,
   PRIMARY KEY(sigle, id_project),
   FOREIGN KEY(sigle) REFERENCES Course(sigle),
   FOREIGN KEY(id_project) REFERENCES Project(id_project)
);


